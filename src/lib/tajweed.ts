export const getTajweedRule = (id: string) => TAJWEED_RULES.find((r) => r.id === id);

/** تجميع القواعد حسب الفئة */
export function getTajweedCategories() {
  const cats = new Map<string, TajweedRule[]>();
  for (const rule of TAJWEED_RULES) {
    if (!cats.has(rule.category)) cats.set(rule.category, []);
    cats.get(rule.category)!.push(rule);
  }
  return Array.from(cats.entries()).map(([name, rules]) => ({ name, rules }));
}
